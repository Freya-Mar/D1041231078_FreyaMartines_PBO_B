public class LatihanKalkulator_Latihan2 {

    public static void main(String[] args) {

        int a = 25;
        int b = 4;

        System.out.println("=== KALKULATOR === ");
        System.out.println("Angka 1 : " + a);
        System.out.println("Angka 2 : " + b);
        System.out.println("---");
        System.out.println("Penjumlahan : " + (a+b));
        System.out.println("Pengurangan : " + (a-b));
        System.out.println("Perkalian : " + (a*b));
        System.out.println("Pembagian : " + (a/b));
        System.out.println("Sisa Bagi : " +(a%b));
        System.out.println("Pembagian Desimal : " + ((double) a/b));

    }
    
}
