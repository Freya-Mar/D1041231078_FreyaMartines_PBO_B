public class p022WorkingVariable {

    public static void main(String[] args) {

        //Deklarasi dan inisialisasi variable
        int angka = 10;

        //Menampilkan nilai awal variable
        System.out.println("Nilai awal angka : " + angka);

        //Mengubah nilai variable
        angka = 25;

        //Menampikan nilai setelah perubahan
        System.out.println("Nilai angka setelah diubah : " + angka);
    }
    
}
